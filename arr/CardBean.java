package com.jse.arr;

public class CardBean {
	private String kind;
	private String number;
	
	public CardBean(String kind,String number) {
		this.kind = kind;
		this.number = number;
	}
	public void setKind(String kind) {
		this.kind = kind;
	}
	public String getKind() {
		return kind;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getNumber() {
		return number;
	}
	
	
}
