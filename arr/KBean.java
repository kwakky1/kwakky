package com.jse.arr;

public class KBean {
	private String shape;
	private int number;
	
	public KBean(String shape, int number) {
		this.shape = shape;
		this.number = number;
	}
	public void setShape(String shape) {
		this.shape = shape;
	}
	public String getShape() {
	return shape;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public int getNumber() {
	return number;
	}
}
